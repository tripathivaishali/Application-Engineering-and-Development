    private int calculateAge(Date dob) {
        Date currentDate = new Date();
        float diffDays = (currentDate.getTime() - dob.getTime()) / ((1000 * 60 * 60 * 24));
        int diffYears = (int) (diffDays / 365);
        return diffYears;
    }

